    public void cetakibuhamil() {
        try {
            String path = "./src/report/ibuhamil.jasper"; // letakpenyimpanan report
            HashMap parameter = new HashMap();
            parameter.put("parameterId", iddaftaribuhamil.getText());
            JasperPrint print = JasperFillManager.fillReport(path, parameter, conn);
            JasperViewer.viewReport(print, false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "DokumenTidak Ada " + ex);
        }
    }
    
    private void autonumber(){
        try {
            String sql = "SELECT id FROM pelayananibuhamil order by id asc";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            iddaftaribuhamil.setText("1");
            while (rs.next()) {
                String id_transaksi = rs.getString("id");
                int AN = Integer.parseInt(id_transaksi) + 1;
                String ANS = Integer.toString(AN);
                iddaftaribuhamil.setText(ANS);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Auto Number Gagal" + e);
        }
    }
    
    protected void datatableibuhamil(){
        Object[] Baris ={"No Layanan","Id Ibu","Nama Ibu","Usia","Berat","Usia Kehamilan","Tensi","Ket."};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem=cariibuhamiltabel.getText();

        try {
            String sql = "SELECT pelayananibuhamil.no_layanan, ibuhamil.id, ibuhamil.nama, ibuhamil.usia, pelayananibuhamil.berat, pelayananibuhamil.usia_hamil, pelayananibuhamil.tensi, pelayananibuhamil.keterangan FROM pelayananibuhamil INNER JOIN ibuhamil ON pelayananibuhamil.id_ibuhamil = ibuhamil.id where ibuhamil.id like '%"+cariitem+"%' or ibuhamil.nama like '%"+cariitem+"%' order by ibuhamil.id asc";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5),
                    hasil.getString(6),
                    hasil.getString(7),
                    hasil.getString(8),
                });
            }
            tabelibuhamil.setModel(tabmode);
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "data gagal dipanggil"+e);
        }
    }
     
